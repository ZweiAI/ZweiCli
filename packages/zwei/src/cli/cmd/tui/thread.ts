import { cmd } from "@/cli/cmd/cmd"
import { withNetworkOptions, type NetworkOptions } from "@/cli/network"

export type ThreadArgs = NetworkOptions & {
  project?: string
  model?: string
  continue?: boolean
  session?: string
  fork?: boolean
  prompt?: string
  agent?: string
}

export const TuiThreadCommand = cmd({
  command: "$0 [project]",
  describe: "start opencode tui",
  builder: (yargs) =>
    withNetworkOptions(yargs)
      .positional("project", {
        type: "string",
        describe: "path to start opencode in",
      })
      .option("model", {
        type: "string",
        alias: ["m"],
        describe: "model to use in the format of provider/model",
      })
      .option("continue", {
        alias: ["c"],
        describe: "continue the last session",
        type: "boolean",
      })
      .option("session", {
        alias: ["s"],
        type: "string",
        describe: "session id to continue",
      })
      .option("fork", {
        type: "boolean",
        describe: "fork the session when continuing (use with --continue or --session)",
      })
      .option("prompt", {
        type: "string",
        describe: "prompt to use",
      })
      .option("agent", {
        type: "string",
        describe: "agent to use",
      }),
  handler: async (args) => {
    const { handleThread } = await import("./thread-impl")
    await handleThread(args as ThreadArgs)
  },
})
